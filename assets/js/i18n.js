document.addEventListener("DOMContentLoaded", () => {
    const langToggle = document.getElementById("lang-toggle");
    
    // Check local storage or default to English
    let currentLang = localStorage.getItem("ebru_lang") || "en";

    function applyTranslations(lang) {
        document.querySelectorAll("[data-i18n]").forEach(el => {
            const key = el.getAttribute("data-i18n");
            if (translations[lang] && translations[lang][key]) {
                el.innerHTML = translations[lang][key];
            }
        });
        document.documentElement.lang = lang;
        if(langToggle) {
            langToggle.textContent = lang === "en" ? "EN" : "ES";
        }
        if(window.lucide) {
            lucide.createIcons(); // rebuild icons if replaced in HTML
        }
    }

    if(langToggle) {
        langToggle.addEventListener("click", (e) => {
            e.preventDefault();
            currentLang = currentLang === "en" ? "es" : "en";
            localStorage.setItem("ebru_lang", currentLang);
            applyTranslations(currentLang);
        });
    }

    // Apply immediately on load
    applyTranslations(currentLang);
});
